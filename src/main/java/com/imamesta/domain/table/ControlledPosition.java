package com.imamesta.domain.table;

public class ControlledPosition {
	
	private Double x;
	
	private Double y;
	
	public ControlledPosition() {
		
	}

	public Double getX() {
		return x;
	}

	public void setX(Double x) {
		this.x = x;
	}

	public Double getY() {
		return y;
	}

	public void setY(Double y) {
		this.y = y;
	}
}
